            根据汽车行业专家们的预测，随着我国经济的持续高速发展和人们消费观念的改变，中国将成为世界轿车的最大消费国之一，即我国轿车保有量在未来的一二十年里将会有飞速提高。在不久的将来，开车将会是人们普遍掌握的生活技能，轿车也不再是特权人士的标志，而将是人们出门的代步工具。那么当人们拥有一辆自己的爱车时，无疑会翔倍至。汽车的平时清洁护理和定期美容保养，必然成为人们日常的消费内容。
