        <div>汽车美容护理对大多数车主来说，还是一个比较陌生的概念。一批摇抹布的擦车族，依然使用洗衣粉、洗涤灵（对车漆有酸、碱腐蚀作用），一桶桶脏水污染着城市环境。据统计，每年落入擦车族手中的擦车费就有三个亿。</div>
        <div>另外，由于汽车美容护理业具有灵活、操作简单、利润较高、风险较低等特点，因此国内的大量洗车店、汽车配件精品店、轮胎店、汽修厂及个人蜂拥进入汽车美容市场，以争得市场上的份额，致使市场竞争日趋激烈。</div>
        <div>
            但是由于汽车美容专业化要求非常高，是一个全新的概念，所以它与一般的洗车擦车、打蜡上光等有着本质的区别。根据调查，市场上许多汽车美容店是“无专业正规培训”、“无专业名牌产品”、无专业机械设备“、无服务质量保证”的四无状况。而且，由于国家对这个新兴行业的管理制度尚不健全，加上消费者对本行业缺乏了解，所以市场上相继出现了一些东拼西凑起来的汽车美容用品。这些杂牌以巧妙的伪装和华丽的广告宣传，打着进口专业品牌的旗号，用一些假冒伪劣产品来坑骗广大用户。但随着汽车美容护理市场不断规范和人们消费意识的提高，伪劣产品将无处立足，无专业服务的汽车美容店若不改变现状也将会被淘汰。如何在汽车美容护理业立足、发展并壮大，已成为每一个业内人士所关注的问题。
